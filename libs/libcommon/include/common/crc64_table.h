static inline uint64_t update1(uint64_t state, uint8_t x)
{
    return (state >> 8) ^ TABLE_0[x ^ (static_cast<uint8_t>(state))];
}

static inline uint64_t update16(uint64_t state, const std::array<uint8_t, 16> & slice)
{
    union
    {
        uint64_t num{};
        uint8_t s[8];
    };

#if defined(__BIG_ENDIAN__)
    num = __builtin_bswap64(state);
#else
    num = state;
#endif

    return TABLE_0[slice[15]] ^ TABLE_1[slice[14]] ^ TABLE_2[slice[13]] ^ TABLE_3[slice[12]] ^ TABLE_4[slice[11]] ^ TABLE_5[slice[10]]
        ^ TABLE_6[slice[9]] ^ TABLE_7[slice[8]] ^ TABLE_8[slice[7] ^ s[7]] ^ TABLE_9[slice[6] ^ s[6]] ^ TABLE_10[slice[5] ^ s[5]]
        ^ TABLE_11[slice[4] ^ s[4]] ^ TABLE_12[slice[3] ^ s[3]] ^ TABLE_13[slice[2] ^ s[2]] ^ TABLE_14[slice[1] ^ s[1]]
        ^ TABLE_15[slice[0] ^ s[0]];
}

static inline uint64_t update_table(uint64_t state, const void * src, size_t length)
{
    auto address = reinterpret_cast<uintptr_t>(src);
    const auto * ptr = reinterpret_cast<const uint8_t *>(src);
    auto prefix = (-address) & 15;
    if (prefix >= length)
    {
        for (size_t i = 0; i < length; ++i, ++ptr)
        {
            state = update1(state, *ptr);
        };
        return state;
    }
    auto suffix = (length - prefix) & 15;
    auto middle = length - prefix - suffix;
    for (size_t i = 0; i < prefix; ++i, ++ptr)
    {
        state = update1(state, *ptr);
    };
    for (size_t i = 0; i < middle; i += 16, ptr += 16)
    {
        const auto * aligned = reinterpret_cast<const uint8_t *>(__builtin_assume_aligned(ptr, 16));
        std::array<uint8_t, 16> slice{
            aligned[0],
            aligned[1],
            aligned[2],
            aligned[3],
            aligned[4],
            aligned[5],
            aligned[6],
            aligned[7],
            aligned[8],
            aligned[9],
            aligned[10],
            aligned[11],
            aligned[12],
            aligned[13],
            aligned[14],
            aligned[15],
        };
        state = update16(state, slice);
    }
    for (size_t i = 0; i < suffix; ++i, ++ptr)
    {
        state = update1(state, *ptr);
    }
    return state;
}

